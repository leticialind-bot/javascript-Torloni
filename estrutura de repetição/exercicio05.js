// imprimir números pares de 0 a 20 utilizando o loop while

let numero = 0;  //inicializacao

//condição
while (numero <= 20) {
    //só entra no corpo se a condição for verdadeira
    console.log(numero);
    // numero = numero + 2;
    numero += 2; //soma de 2 em 2, ou seja, somente numeros pares 
}